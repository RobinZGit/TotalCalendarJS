  function openTrainHelpDialog(ev){
    if (ev) {
      ev.preventDefault();
      ev.stopPropagation();
    }
    var overlay = document.getElementById("idTrainHelpOverlay");
    var body = document.getElementById("idTrainHelpBody");
    if (body)
      body.innerHTML = getTrainingSetupHelpHtml();
    if (overlay)
      overlay.hidden = false;
  }

  function closeTrainHelpDialog(){
    var overlay = document.getElementById("idTrainHelpOverlay");
    if (overlay)
      overlay.hidden = true;
  }

  function initEvalParamsDialog(){
    if (initEvalParamsDialog._bound)
      return;
    initEvalParamsDialog._bound = true;
    document.addEventListener("keydown", function (e) {
      if (e.key === "Escape") {
        var evOverlay = document.getElementById("idEvalParamsOverlay");
        if (evOverlay && !evOverlay.hidden)
          cancelEvalParamsDialog();
      }
    });
    var evOverlay = document.getElementById("idEvalParamsOverlay");
    if (evOverlay) {
      evOverlay.addEventListener("click", function (e) {
        if (e.target === evOverlay)
          cancelEvalParamsDialog();
      });
    }
  }

  function initTrainHelpDialog(){
    if (initTrainHelpDialog._bound)
      return;
    initTrainHelpDialog._bound = true;
    document.addEventListener("keydown", function (e) {
      if (e.key === "Escape") {
        var overlay = document.getElementById("idTrainHelpOverlay");
        if (overlay && !overlay.hidden)
          closeTrainHelpDialog();
      }
    });
    var overlay = document.getElementById("idTrainHelpOverlay");
    if (overlay) {
      overlay.addEventListener("click", function (e) {
        if (e.target === overlay)
          closeTrainHelpDialog();
      });
    }
  }
